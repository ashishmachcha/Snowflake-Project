-- -----------------------------------------------------------------------------
-- FACT: FACT_MOVIE_FINANCIALS
-- Grain: One row per movie with financial metrics
-- -----------------------------------------------------------------------------
CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.FACT_MOVIE_FINANCIALS AS
SELECT
    dm.MOVIE_SK,
    dm.MOVIE_ID,
    dm.TITLE,
    dm.BUDGET,
    dm.REVENUE,
    dm.REVENUE - dm.BUDGET AS PROFIT,
    CASE WHEN dm.BUDGET > 0 THEN ROUND(((dm.REVENUE - dm.BUDGET) / dm.BUDGET) * 100, 2) ELSE NULL END AS ROI,
    CASE WHEN dm.REVENUE > 0 THEN ROUND(((dm.REVENUE - dm.BUDGET) / dm.REVENUE) * 100, 2) ELSE NULL END AS PROFIT_MARGIN,
    dm.RELEASE_YEAR,
    dm.IMDB_RATING,
    dm.NUMVOTES,
    dm.TMDB_POPULARITY
FROM MOVIE_PROJECT_DB.GOLD.DIM_MOVIES dm
WHERE dm.BUDGET > 0 AND dm.REVENUE > 0;

