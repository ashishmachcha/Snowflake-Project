-- -----------------------------------------------------------------------------
-- BUSINESS REQUIREMENT 7: Cast and Crew Analysis
-- -----------------------------------------------------------------------------

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.ACTOR_PERFORMANCE AS
SELECT
    PRIMARYNAME,
    COUNT(DISTINCT TCONST) AS MOVIE_COUNT,
    ROUND(AVG(IMDB_RATING), 2) AS AVG_RATING,
    SUM(NUMVOTES) AS TOTAL_VOTES
FROM MOVIE_PROJECT_DB.SILVER.IMDB_CAST_CREW
WHERE CATEGORY IN ('actor', 'actress')
GROUP BY PRIMARYNAME
HAVING COUNT(DISTINCT TCONST) >= 5;

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.DIRECTOR_PERFORMANCE AS
SELECT
    PRIMARYNAME,
    COUNT(DISTINCT TCONST) AS MOVIE_COUNT,
    ROUND(AVG(IMDB_RATING), 2) AS AVG_RATING,
    SUM(NUMVOTES) AS TOTAL_VOTES
FROM MOVIE_PROJECT_DB.SILVER.IMDB_CAST_CREW
WHERE CATEGORY = 'director'
GROUP BY PRIMARYNAME
HAVING COUNT(DISTINCT TCONST) >= 3;

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.ACTOR_DIRECTOR_COMBOS AS
SELECT
    a.PRIMARYNAME AS ACTOR,
    d.PRIMARYNAME AS DIRECTOR,
    COUNT(DISTINCT a.TCONST) AS MOVIES_TOGETHER,
    ROUND(AVG(a.IMDB_RATING), 2) AS AVG_RATING
FROM MOVIE_PROJECT_DB.SILVER.IMDB_CAST_CREW a
JOIN MOVIE_PROJECT_DB.SILVER.IMDB_CAST_CREW d ON a.TCONST = d.TCONST
WHERE a.CATEGORY IN ('actor', 'actress') AND d.CATEGORY = 'director'
GROUP BY a.PRIMARYNAME, d.PRIMARYNAME
HAVING COUNT(DISTINCT a.TCONST) >= 2;

-- -----------------------------------------------------------------------------
-- BUSINESS REQUIREMENT 8: User Engagement Analytics
-- -----------------------------------------------------------------------------

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.ENGAGEMENT_BY_GENRE AS
SELECT
    TRIM(g.VALUE::STRING) AS GENRE,
    COUNT(*) AS RATING_COUNT,
    COUNT(DISTINCT USER_ID) AS UNIQUE_USERS,
    ROUND(AVG(RATING), 2) AS AVG_RATING
FROM MOVIE_PROJECT_DB.SILVER.MOVIELENS_USER_RATINGS,
    LATERAL FLATTEN(INPUT => SPLIT(GENRES, '|')) g
WHERE GENRES IS NOT NULL
GROUP BY TRIM(g.VALUE::STRING)
ORDER BY RATING_COUNT DESC;

-- -----------------------------------------------------------------------------
-- BUSINESS REQUIREMENT 9: Pipeline Monitoring
-- -----------------------------------------------------------------------------

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.PIPELINE_HEALTH AS
SELECT 'TITLE_BASICS' AS SOURCE_TABLE, COUNT(*) AS ROW_COUNT,
       SUM(CASE WHEN PRIMARYTITLE IS NULL THEN 1 ELSE 0 END) AS NULL_KEY_FIELD,
       SUM(CASE WHEN STARTYEAR IS NULL THEN 1 ELSE 0 END) AS NULL_SECONDARY_FIELD
FROM MOVIE_PROJECT_DB.BRONZE_API_IMDB.TITLE_BASICS
UNION ALL
SELECT 'TITLE_RATINGS', COUNT(*),
       SUM(CASE WHEN TCONST IS NULL THEN 1 ELSE 0 END),
       SUM(CASE WHEN AVERAGERATING IS NULL THEN 1 ELSE 0 END)
FROM MOVIE_PROJECT_DB.BRONZE_API_IMDB.TITLE_RATINGS
UNION ALL
SELECT 'MOVIES_METADATA', COUNT(*),
       SUM(CASE WHEN TITLE IS NULL THEN 1 ELSE 0 END),
       SUM(CASE WHEN TRY_CAST(ID AS NUMBER) IS NULL THEN 1 ELSE 0 END)
FROM MOVIE_PROJECT_DB.BRONZE_API_KAGGLE2.MOVIES_METADATA
UNION ALL
SELECT 'MOVIELENS_RATINGS', COUNT(*),
       SUM(CASE WHEN USER_ID IS NULL THEN 1 ELSE 0 END),
       SUM(CASE WHEN RATING IS NULL THEN 1 ELSE 0 END)
FROM MOVIE_PROJECT_DB.BRONZE_API_MOVIE_LENS.RATINGS
UNION ALL
SELECT 'NETFLIX', COUNT(*),
       SUM(CASE WHEN TITLE IS NULL THEN 1 ELSE 0 END),
       SUM(CASE WHEN TYPE IS NULL THEN 1 ELSE 0 END)
FROM MOVIE_PROJECT_DB.BRONZE_API.NETFLIX
UNION ALL
SELECT 'CREDITS', COUNT(*),
       SUM(CASE WHEN ID IS NULL THEN 1 ELSE 0 END),
       SUM(CASE WHEN CAST_COL IS NULL THEN 1 ELSE 0 END)
FROM MOVIE_PROJECT_DB.BRONZE_API_KAGGLE2.CREDITS;

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.PIPELINE_DUPLICATES AS
SELECT 'TITLE_RATINGS' AS SOURCE, COUNT(*) - COUNT(DISTINCT TCONST) AS DUPLICATE_COUNT
FROM MOVIE_PROJECT_DB.BRONZE_API_IMDB.TITLE_RATINGS
UNION ALL
SELECT 'MOVIES_METADATA', COUNT(*) - COUNT(DISTINCT ID)
FROM MOVIE_PROJECT_DB.BRONZE_API_KAGGLE2.MOVIES_METADATA
UNION ALL
SELECT 'NETFLIX', COUNT(*) - COUNT(DISTINCT SHOW_ID)
FROM MOVIE_PROJECT_DB.BRONZE_API.NETFLIX;

