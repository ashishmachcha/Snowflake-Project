-- -----------------------------------------------------------------------------
-- DIMENSION: DIM_MOVIES
-- Grain: One row per movie
-- -----------------------------------------------------------------------------
CREATE OR REPLACE TABLE MOVIE_PROJECT_DB.GOLD.DIM_MOVIES AS
SELECT
    ROW_NUMBER() OVER (ORDER BY sm.MOVIE_ID) AS MOVIE_SK,
    sm.MOVIE_ID,
    sm.TITLE,
    sm.ORIGINAL_LANGUAGE,
    sm.STATUS,
    sm.RUNTIME,
    sm.RELEASE_DATE,
    sm.RELEASE_YEAR,
    sm.BUDGET,
    sm.REVENUE,
    sm.IMDB_ID,
    im.TCONST,
    im.IMDB_RATING,
    im.NUMVOTES,
    sm.POPULARITY AS TMDB_POPULARITY,
    sm.VOTE_AVERAGE AS TMDB_VOTE_AVERAGE,
    sm.VOTE_COUNT AS TMDB_VOTE_COUNT,
    sm.PRODUCTION_COMPANIES,
    sm.GENRES AS GENRES_JSON
FROM MOVIE_PROJECT_DB.SILVER.MOVIES_METADATA sm
LEFT JOIN MOVIE_PROJECT_DB.SILVER.IMDB_MOVIES im ON sm.IMDB_ID = im.TCONST;

-- -----------------------------------------------------------------------------
-- DIMENSION: DIM_GENRES
-- Grain: One row per genre
-- -----------------------------------------------------------------------------
CREATE OR REPLACE TABLE MOVIE_PROJECT_DB.GOLD.DIM_GENRES AS
SELECT
    ROW_NUMBER() OVER (ORDER BY GENRE) AS GENRE_SK,
    GENRE
FROM (
    SELECT DISTINCT g.VALUE:name::STRING AS GENRE
    FROM MOVIE_PROJECT_DB.SILVER.MOVIES_METADATA,
        LATERAL FLATTEN(INPUT => PARSE_JSON(GENRES)) g
    WHERE g.VALUE:name IS NOT NULL
    UNION
    SELECT DISTINCT TRIM(VALUE::STRING) AS GENRE
    FROM MOVIE_PROJECT_DB.SILVER.IMDB_MOVIES,
        LATERAL FLATTEN(INPUT => SPLIT(GENRES, ','))
    WHERE GENRES IS NOT NULL
)
WHERE GENRE IS NOT NULL AND GENRE != '';

-- -----------------------------------------------------------------------------
-- DIMENSION: DIM_ACTORS
-- Grain: One row per actor/actress
-- -----------------------------------------------------------------------------
CREATE OR REPLACE TABLE MOVIE_PROJECT_DB.GOLD.DIM_ACTORS AS
SELECT
    ROW_NUMBER() OVER (ORDER BY PRIMARYNAME) AS ACTOR_SK,
    NCONST,
    PRIMARYNAME,
    CATEGORY
FROM (
    SELECT DISTINCT NCONST, PRIMARYNAME, CATEGORY
    FROM MOVIE_PROJECT_DB.SILVER.IMDB_CAST_CREW
    WHERE CATEGORY IN ('actor', 'actress')
);

-- -----------------------------------------------------------------------------
-- DIMENSION: DIM_DIRECTORS
-- Grain: One row per director
-- -----------------------------------------------------------------------------
CREATE OR REPLACE TABLE MOVIE_PROJECT_DB.GOLD.DIM_DIRECTORS AS
SELECT
    ROW_NUMBER() OVER (ORDER BY PRIMARYNAME) AS DIRECTOR_SK,
    NCONST,
    PRIMARYNAME
FROM (
    SELECT DISTINCT NCONST, PRIMARYNAME
    FROM MOVIE_PROJECT_DB.SILVER.IMDB_CAST_CREW
    WHERE CATEGORY = 'director'
);

-- -----------------------------------------------------------------------------
-- DIMENSION: DIM_DATE
-- Grain: One row per date (1950 to 2030)
-- -----------------------------------------------------------------------------
CREATE OR REPLACE TABLE MOVIE_PROJECT_DB.GOLD.DIM_DATE AS
SELECT
    TO_NUMBER(TO_CHAR(d.DATE_VAL, 'YYYYMMDD')) AS DATE_SK,
    d.DATE_VAL AS FULL_DATE,
    YEAR(d.DATE_VAL) AS YEAR,
    QUARTER(d.DATE_VAL) AS QUARTER,
    MONTH(d.DATE_VAL) AS MONTH,
    MONTHNAME(d.DATE_VAL) AS MONTH_NAME,
    DAY(d.DATE_VAL) AS DAY,
    DAYOFWEEK(d.DATE_VAL) AS DAY_OF_WEEK,
    DAYNAME(d.DATE_VAL) AS DAY_NAME,
    WEEKOFYEAR(d.DATE_VAL) AS WEEK_OF_YEAR
FROM (
    SELECT DATEADD(DAY, SEQ4(), '1950-01-01'::DATE) AS DATE_VAL
    FROM TABLE(GENERATOR(ROWCOUNT => 29220))
) d;

-- -----------------------------------------------------------------------------
-- DIMENSION: DIM_COUNTRIES
-- Grain: One row per country
-- -----------------------------------------------------------------------------
CREATE OR REPLACE TABLE MOVIE_PROJECT_DB.GOLD.DIM_COUNTRIES AS
SELECT
    ROW_NUMBER() OVER (ORDER BY COUNTRY) AS COUNTRY_SK,
    COUNTRY
FROM (
    SELECT DISTINCT TRIM(c.VALUE::STRING) AS COUNTRY
    FROM MOVIE_PROJECT_DB.SILVER.NETFLIX_CATALOG,
        LATERAL FLATTEN(INPUT => SPLIT(COUNTRY, ',')) c
    WHERE COUNTRY IS NOT NULL
)
WHERE COUNTRY IS NOT NULL AND COUNTRY != '';

