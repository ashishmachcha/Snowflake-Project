-- -----------------------------------------------------------------------------
-- BRIDGE: BRIDGE_MOVIE_GENRE (many-to-many between movies and genres)
-- -----------------------------------------------------------------------------
CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.BRIDGE_MOVIE_GENRE AS
SELECT
    dm.MOVIE_SK,
    dm.MOVIE_ID,
    dm.TITLE,
    dg.GENRE_SK,
    dg.GENRE
FROM MOVIE_PROJECT_DB.GOLD.DIM_MOVIES dm,
    LATERAL FLATTEN(INPUT => PARSE_JSON(dm.GENRES_JSON)) g
JOIN MOVIE_PROJECT_DB.GOLD.DIM_GENRES dg ON dg.GENRE = g.VALUE:name::STRING
WHERE dm.GENRES_JSON IS NOT NULL;
