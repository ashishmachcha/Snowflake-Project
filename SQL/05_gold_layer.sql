CREATE SCHEMA IF NOT EXISTS MOVIE_PROJECT_DB.GOLD;

-- -----------------------------------------------------------------------------
-- BUSINESS REQUIREMENT 1: Movie Popularity Analysis
-- -----------------------------------------------------------------------------

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.TOP_MOVIES_BY_VOTES AS
SELECT TITLE, NUMVOTES, IMDB_RATING, RELEASE_YEAR, GENRES
FROM MOVIE_PROJECT_DB.SILVER.IMDB_MOVIES
ORDER BY NUMVOTES DESC
LIMIT 100;

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.TOP_MOVIES_BY_RATING AS
SELECT TITLE, IMDB_RATING, NUMVOTES, RELEASE_YEAR, GENRES
FROM MOVIE_PROJECT_DB.SILVER.IMDB_MOVIES
ORDER BY IMDB_RATING DESC
LIMIT 100;

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.MOST_POPULAR_BY_YEAR AS
SELECT * FROM (
    SELECT
        TITLE, RELEASE_YEAR, NUMVOTES, IMDB_RATING,
        ROW_NUMBER() OVER (PARTITION BY RELEASE_YEAR ORDER BY NUMVOTES DESC) AS RN
    FROM MOVIE_PROJECT_DB.SILVER.IMDB_MOVIES
)
WHERE RN = 1;

-- -----------------------------------------------------------------------------
-- BUSINESS REQUIREMENT 2: Genre Performance Analysis
-- -----------------------------------------------------------------------------

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.GENRE_PERFORMANCE AS
SELECT
    g.VALUE:name::STRING AS GENRE,
    COUNT(*) AS TOTAL_MOVIES,
    ROUND(AVG(VOTE_AVERAGE), 2) AS AVG_RATING,
    ROUND(AVG(REVENUE), 0) AS AVG_REVENUE,
    ROUND(AVG(BUDGET), 0) AS AVG_BUDGET,
    ROUND(AVG(REVENUE) - AVG(BUDGET), 0) AS AVG_PROFIT,
    ROUND(AVG(RUNTIME), 1) AS AVG_RUNTIME,
    SUM(VOTE_COUNT) AS TOTAL_VOTES,
    RANK() OVER (ORDER BY SUM(VOTE_COUNT) DESC) AS GENRE_POPULARITY_RANK
FROM MOVIE_PROJECT_DB.SILVER.MOVIES_METADATA,
    LATERAL FLATTEN(INPUT => PARSE_JSON(GENRES)) g
WHERE g.VALUE:name IS NOT NULL
GROUP BY g.VALUE:name::STRING;

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.GENRE_BY_YEAR AS
SELECT
    g.VALUE:name::STRING AS GENRE,
    RELEASE_YEAR,
    COUNT(*) AS MOVIE_COUNT,
    ROUND(AVG(VOTE_AVERAGE), 2) AS AVG_RATING,
    ROUND(AVG(REVENUE), 0) AS AVG_REVENUE
FROM MOVIE_PROJECT_DB.SILVER.MOVIES_METADATA,
    LATERAL FLATTEN(INPUT => PARSE_JSON(GENRES)) g
WHERE g.VALUE:name IS NOT NULL AND RELEASE_YEAR IS NOT NULL
GROUP BY g.VALUE:name::STRING, RELEASE_YEAR;

-- -----------------------------------------------------------------------------
-- BUSINESS REQUIREMENT 3: Movie Financial Performance
-- -----------------------------------------------------------------------------

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.MOVIE_FINANCIALS AS
SELECT
    MOVIE_ID,
    TITLE,
    BUDGET,
    REVENUE,
    REVENUE - BUDGET AS PROFIT,
    ROUND(((REVENUE - BUDGET) / NULLIF(BUDGET, 0)) * 100, 2) AS ROI,
    ROUND(REVENUE / NULLIF(BUDGET, 0), 2) AS REVENUE_TO_BUDGET_RATIO,
    ROUND(((REVENUE - BUDGET) / NULLIF(REVENUE, 0)) * 100, 2) AS PROFIT_MARGIN,
    RELEASE_YEAR,
    GENRES,
    PRODUCTION_COMPANIES
FROM MOVIE_PROJECT_DB.SILVER.MOVIES_METADATA
WHERE BUDGET > 0 AND REVENUE > 0;

-- -----------------------------------------------------------------------------
-- BUSINESS REQUIREMENT 4: Streaming Catalog Analysis (Netflix)
-- -----------------------------------------------------------------------------

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.NETFLIX_CONTENT_SUMMARY AS
SELECT
    COUNT(*) AS TOTAL_TITLES,
    SUM(CASE WHEN TYPE = 'Movie' THEN 1 ELSE 0 END) AS MOVIES_COUNT,
    SUM(CASE WHEN TYPE = 'TV Show' THEN 1 ELSE 0 END) AS TV_SHOWS_COUNT
FROM MOVIE_PROJECT_DB.SILVER.NETFLIX_CATALOG;

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.NETFLIX_ADDED_BY_YEAR AS
SELECT
    ADDED_YEAR,
    COUNT(*) AS TITLES_ADDED,
    SUM(CASE WHEN TYPE = 'Movie' THEN 1 ELSE 0 END) AS MOVIES_ADDED,
    SUM(CASE WHEN TYPE = 'TV Show' THEN 1 ELSE 0 END) AS TV_SHOWS_ADDED
FROM MOVIE_PROJECT_DB.SILVER.NETFLIX_CATALOG
WHERE ADDED_YEAR IS NOT NULL
GROUP BY ADDED_YEAR;

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.NETFLIX_BY_COUNTRY AS
SELECT
    TRIM(c.VALUE::STRING) AS COUNTRY,
    COUNT(*) AS TITLE_COUNT
FROM MOVIE_PROJECT_DB.SILVER.NETFLIX_CATALOG,
    LATERAL FLATTEN(INPUT => SPLIT(COUNTRY, ',')) c
WHERE COUNTRY IS NOT NULL
GROUP BY TRIM(c.VALUE::STRING)
ORDER BY TITLE_COUNT DESC;

CREATE OR REPLACE VIEW MOVIE_PROJECT_DB.GOLD.NETFLIX_BY_GENRE AS
SELECT
    TRIM(g.VALUE::STRING) AS GENRE,
    COUNT(*) AS TITLE_COUNT
FROM MOVIE_PROJECT_DB.SILVER.NETFLIX_CATALOG,
    LATERAL FLATTEN(INPUT => SPLIT(GENRE, ',')) g
WHERE GENRE IS NOT NULL
GROUP BY TRIM(g.VALUE::STRING)
ORDER BY TITLE_COUNT DESC;

